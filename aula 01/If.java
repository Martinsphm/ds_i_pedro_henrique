public class If {
    public static void main(String[] args) {
        int x = 20;
        int y = 20;

        if (x > y ) {
            System.out.println("x e maior que y");
        } else if (x == y) {
            System.out.println("x e y sao igual");
        } else {
            System.out.println("y e maior que x");
        }
    }
}
